  },
  methods: {
    sendMessage() {
      let valid = true;
      if (
        this.comment == "" ||
        this.name == "" ||
        this.email == "" ||
        this.title == ""
      ) {
        valid = false;
      }

      if (valid) {
        const BOT_KEY = "1561468131:AAGJ67rQ5kwuMDpPEj3HxfOwj2cUbsKvgBM";
        const CHAT_ID = "-1001434033498";
        let msgText = encodeURI(
          "<b>Name </b>: " +
            this.name +
            "\n<b>Email </b>: " +
            this.email +
            "\n<b>Title </b>: " +
            this.title +
            "\n<b>Comment </b>: " +
            this.comment
        );

        axios
          .get(
            "https://api.telegram.org/bot" +
              BOT_KEY +
              "/sendMessage?chat_id=" +
              CHAT_ID +
              "&parse_mode=html&text=" +
              msgText
          )
          .then((resp) => {
            if (resp.data.ok) {
              this.name = "";
              this.email = "";
              this.title = "";
              this.comment = "";
              Vue.$toast.success("Thanks. You will be contacted soon!", {
                position: "top",
              });
            } else {
              Vue.$toast.error("Some error occures", { position: "top" });
            }
          })
          .catch((err) => {
            console.log(err);
            Vue.$toast.error(err);
          });
      } else {
        Vue.$toast.error("Fill all fields", { position: "top" });
      }
    },
  },
};
